# Logic Operater là các toán tử dùng để so sánh giữa các giá trị hoặc biểu thức logic

# Ví dụ về toán tử so sánh
# a = 10
# b = 5
# print(a > b) # => True
# print(a < b) # => False

# Ví dụ về toán tử logic
# a = 10
# b = 5
# c = 3
# print(a > b and a > c) # => True
# print(a > b or a < c) # => True
# print(not a > b) # => False

