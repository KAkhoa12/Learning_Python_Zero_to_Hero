# dictionarites là một cấu trúc dữ liệu mà mỗi phần tử của nó là một cặp key-value.

# Ví dụ: Dictionary
dictionary = {
    "name": "John",
    "age": 30,
    "city": "New York"
}

print(dictionary.items())
print(dictionary.keys())
print(dictionary.values())
print(dictionary["name"])
print(dictionary.get("name"))
