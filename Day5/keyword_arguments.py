# keyword arguments là một cách để truyền tham số vào hàm dựa trên tên của tham số đó. Khi sử dụng keyword arguments, thứ tự của tham số không cần phải giống với thứ tự khai báo của hàm.

# Ví dụ:
# def greet(name, age):
#     print(f"Hello, {name}! You are {age} years old.")

# greet(age=20, name="Alice")  # In ra "Hello, Alice! You are 20 years old."
# greet(name="Bob", age=25)  # In ra "Hello, Bob! You are 25 years old."